            return new MembershipUser(
                "AwcMembershipProvider",
                user.EmailAddress,
                user.UserId,
                user.EmailAddress,
                string.Empty,
                string.Empty,
                user.IsActive,
                false,
                user.CreationDate,
                user.LastLoginDate.GetValueOrDefault(),
